BOLD_RULES_BRIEF = """## 加粗规则（严格遵守，违反即失败）

### 格式约定（最重要）
- 所有加粗**必须使用 HTML 标签** `<b>...</b>`，**不要用 Markdown** `**...**`。
- 原文中已有的数字必须**按原始字面保留**：原文写 `9000+` 就保留 `9000+`，不要自作主张改成 `9k+`；原文 `10,000+` 就保留 `10,000+`，不要改成 `10k+` 或 `1w+`。只允许改数字的加粗范围，不允许改数字本身的表达形式。

A. 必须加粗（结构元素）
  - Section 标题（个人总结 / 项目经历 / 实习经历 / 专业技能 / 教育背景）
  - bullet label（冒号前的短语），用 `<b>内容生产线：</b>` 的 HTML 形式
  - 技能分类 label（「AI 工具：」「内容生产：」等），同样用 `<b>AI 工具：</b>`
  - 教育分类 label（「核心课程：」「荣誉：」），同样用 `<b>核心课程：</b>`

B. 结果型数据 — 只加粗「纯数字部分」，不加粗单位、名词、动词
  正确示例：
    <b>9,000+</b> 粉（不是 <b>9,000+ 粉</b>）
    <b>60+</b> 条 Prompt 模板
    <b>18.9%</b>、<b>4%</b> 提升到 <b>10%</b>
    <b>2w+</b> 播放
    <b>30</b>+ 页（+ 号在 b 标签外）
    <b>100+</b> 国内外 AI 公司
    <b>10,000+</b> 自然曝光
    <b>200+</b> 粉增长

C. 数字对比段整体加粗（含单位）
  - <b>0 → 9,000+</b>
  - <b>1,240 → 1,644</b>
  - <b>1,048 万 → 1,262 万（+20.6%）</b>

D. 过程/规格描述 — 整句不加粗（即便包含数字）
  - 「单条内容生产时间从 40 分钟压到 10 分钟以内」(效率过程)
  - 「单条 JD 定制简历 30 秒产出」(规格)
  - 「每日自动拉 50+ 条资讯、筛选 5-10 条素材」(中间流程)
  - 「拆出 6 个核心模块」(配置)

E. 永不加粗
  - 定性形容词：首批 / 头部 / 核心 / 深度
  - 动词：搭建 / 重做 / 拆解 / 独立完成
  - 工具/平台名：Claude Code、Kimi、TikTok、PhotoRoom
  - 公司名 / 职位名（由结构样式自动处理）

判断标准：这个数字能不能写进复盘报告的 OUTCOME 一栏？
  - 能（增长率、转化率、绝对规模、商业变现）→ 加粗纯数字
  - 不能（处理速度、规格配置、中间流程量）→ 整句不加粗

【兼容性】若源素材使用旧格式 <b>数字 单位描述</b>，请在输出时自动拆为 <b>数字</b> 单位描述。"""


